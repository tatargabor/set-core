def _setup_v1_project(tmp_path):
    proj = tmp_path / "proj"
    proj.mkdir()
    rt = SetRuntime(str(proj))
    rt.ensure_dirs()

    plan_path = tp.plan_file(rt.orchestration_dir)
    domains_path = os.path.join(rt.orchestration_dir, "orchestration-plan-domains.json")
    digest_dir = proj / "set" / "orchestration" / "digest"
    digest_dir.mkdir(parents=True, exist_ok=True)
    (digest_dir / "REQ-1.md").write_text("# v1 req")

    with open(plan_path, "w") as fh:
        json.dump({
            "plan_version": 1,
            "input_path": "docs/spec-v1.md",
            "changes": [{"name": "v1-change", "scope": "s",
                         "complexity": "M", "phase": 1}],
        }, fh)
    with open(domains_path, "w") as fh:
        json.dump({"domains": [{"name": "v1-domain"}]}, fh)
    return proj, rt


