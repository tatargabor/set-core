def test_rotate_plan_handles_corrupt_plan(tmp_path, caplog):
    import logging
    proj = tmp_path / "proj"
    proj.mkdir()
    rt = SetRuntime(str(proj))
    rt.ensure_dirs()
    with open(tp.plan_file(rt.orchestration_dir), "w") as fh:
        fh.write("{ broken")
    with caplog.at_level(logging.WARNING, logger="set_orch.engine"):
        result = _rotate_plan_and_digest_for_new_lineage(
            str(proj), "docs/spec-v2.md",
        )
    assert result is None
    assert any("rotate_plan" in rec.message for rec in caplog.records)


