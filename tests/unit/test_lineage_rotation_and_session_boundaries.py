def _make_cycle_file(proj, n, *, session_id, lineage="docs/spec.md", ts=None):
    if ts is None:
        ts = f"2026-01-{n:02d}T00:00:00+00:00"
    path = proj / f"orchestration-events-cycle{n}.jsonl"
    with open(path, "w") as fh:
        fh.write(json.dumps({
            "type": "CYCLE_HEADER",
            "ts": ts,
            "cycle": n,
            "spec_lineage_id": lineage,
            "sentinel_session_id": session_id,
            "plan_version": 1,
        }) + "\n")
        fh.write(json.dumps({
            "type": "DISPATCH", "ts": ts, "change": f"c-{n}",
        }) + "\n")


def test_session_boundaries_emit_one_marker_per_transition(tmp_path):
    proj = tmp_path / "proj"
    proj.mkdir()
    _make_cycle_file(proj, 1, session_id="session-A")
    _make_cycle_file(proj, 2, session_id="session-A")  # no transition
    _make_cycle_file(proj, 3, session_id="session-B")  # boundary 1
    _make_cycle_file(proj, 4, session_id="session-C")  # boundary 2
    boundaries = _collect_session_boundaries(proj)
    # Two transitions → two markers.
    assert len(boundaries) == 2
    sids = [b["session_id"] for b in boundaries]
    assert sids == ["session-B", "session-C"]


def test_session_boundaries_empty_when_no_cycles(tmp_path):
    proj = tmp_path / "proj"
    proj.mkdir()
    assert _collect_session_boundaries(proj) == []


def test_session_boundaries_empty_for_single_session(tmp_path):
    proj = tmp_path / "proj"
    proj.mkdir()
    _make_cycle_file(proj, 1, session_id="only-one")
    _make_cycle_file(proj, 2, session_id="only-one")
    assert _collect_session_boundaries(proj) == []


def test_session_boundary_carries_lineage_metadata(tmp_path):
    proj = tmp_path / "proj"
    proj.mkdir()
    _make_cycle_file(proj, 1, session_id="A", lineage="docs/spec-v1.md")
    _make_cycle_file(proj, 2, session_id="B", lineage="docs/spec-v2.md")
    out = _collect_session_boundaries(proj)
    assert len(out) == 1
    assert out[0]["spec_lineage_id"] == "docs/spec-v2.md"
    assert out[0]["session_id"] == "B"
