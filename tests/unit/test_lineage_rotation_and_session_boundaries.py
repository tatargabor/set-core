def test_rotate_plan_renames_v1_files_when_v2_starts(tmp_path):
    proj, rt = _setup_v1_project(tmp_path)
    out_slug = _rotate_plan_and_digest_for_new_lineage(
        str(proj), "docs/spec-v2.md",
    )
    assert out_slug == slug("docs/spec-v1.md")

    expected_plan = os.path.join(
        rt.orchestration_dir, f"orchestration-plan-{out_slug}.json",
    )
    expected_domains = os.path.join(
        rt.orchestration_dir, f"orchestration-plan-domains-{out_slug}.json",
    )
    expected_digest = proj / "set" / "orchestration" / f"digest-{out_slug}"
    assert os.path.isfile(expected_plan)
    assert os.path.isfile(expected_domains)
    assert expected_digest.is_dir()
    # Live files are gone (they would be regenerated by the new sentinel).
    assert not os.path.exists(tp.plan_file(rt.orchestration_dir))
    assert not (proj / "set" / "orchestration" / "digest").exists()


def test_rotate_plan_no_op_for_same_lineage(tmp_path):
    proj, rt = _setup_v1_project(tmp_path)
    result = _rotate_plan_and_digest_for_new_lineage(
        str(proj), "docs/spec-v1.md",  # same as on-disk
    )
    assert result is None
    # Live files still in place.
    assert os.path.isfile(tp.plan_file(rt.orchestration_dir))
    assert (proj / "set" / "orchestration" / "digest").is_dir()


